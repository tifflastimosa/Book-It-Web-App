package com.libby.letsbookit.model;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonProperty;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;
import javax.validation.constraints.Positive;


/**
 * A class that represents a stand that is at a scheduled event that can be booked by a vendor
 */

@Entity
@Table(name="stands")
public class Stand {

  // the unique id that is autogenerated
  @Id
  @GeneratedValue(strategy = GenerationType.IDENTITY)
  @JsonProperty
  private Integer id;

  @Column(name = "table_name")
  @JsonProperty
  private String name;

  @Column(name = "table_notes")
  @JsonProperty
  private String notes;

  @Column(name = "booked")
  @JsonProperty
  private Boolean booked;

  @Column(name = "price")
  @JsonProperty
  @Positive
  private Float price = 0.0f;

  // TODO: Verify if this is properly implemented
  @ManyToOne
  @JoinColumn(name="event_id")
  @JsonIgnore
  private Event event;

  @ManyToOne
  @JoinColumn(name = "vendor_id")
  @JsonIgnore
  private Vendor vendor;

  // TODO: Verify that this is right!

  /**
   * Constructor for Stand object.
   *
   * @param name The name of the stand.
   * @param notes The notes, description regarding the stand to be booked by the vendor.
   * @param booked Boolean indicating if the stand is booked or not booked.
   * @param price The cost to book the stand, the default value is 0.0f.
   */
  public Stand(String name, String notes, Boolean booked, Float price) {
    this.name = name;
    this.notes = notes;
    this.booked = booked;
    this.price = price;
  }

  /**
   * Constructor with no parameters for Stand.
   */
  public Stand() {
  }

  /**
   * Gets the id of the stand.
   *
   * @return The id of the stand.
   */
  public Integer getId() {
    return id;
  }

  /**
   * Sets the id of the stand.
   *
   * @param id The id of the stand.
   */
  public void setId(Integer id) {
    this.id = id;
  }



  /**
   * Gets the table name of the stand.
   *
   * @return The table name of the stand.
   */
  public String getName() {
    return name;
  }

  /**
   * Sets the table name of the stand.
   *
   * @param name The table name of the stand.
   */
  public void setName(String name) {
    this.name = name;
  }

  /**
   * Gets the table notes of the stand.
   *
   * @return The table notes of the stand.
   */
  public String getNotes() {
    return notes;
  }

  /**
   * Sets teh table notes of the stand.
   *
   * @param notes The table notes of the stand.
   */
  public void setNotes(String notes) {
    this.notes = notes;
  }

  /**
   * Gets the booking status of the stand.
   *
   * @return True if stand is booked, False otherwise.
   */
  public Boolean getBooked() {
    return booked;
  }

  /**
   * Sets the booking status of the stand.
   *
   * @param booked The booking status of the stand.
   */
  public void setBooked(Boolean booked) {
    this.booked = booked;
  }

  /**
   * Gets the price to book the stand.
   *
   * @return The price to book the stand.
   */
  public Float getPrice() {
    return price;
  }

  /**
   * Sets the price to book the stand.
   *
   * @param price The price to book the stand.
   */
  public void setPrice(Float price) {
    this.price = price;
  }

  /**
   * Gets the event the stand belongs to.
   *
   * @return The associated event.
   */
  public Event getEvent() {
    return event;
  }

  /**
   * Sets the associated event.
   *
   * @param event The associated event.
   */
  public void setEvent(Event event) {
    this.event = event;
  }

  /**
   * Gets the vendor that booked the stand.
   *
   * @return The associated vendor.
   */
  public Vendor getVendor() {
    return vendor;
  }

  /**
   * Sets the vendor that has the stand booked.
   *
   * @param vendor The associated vendor.
   */
  public void setVendor(Vendor vendor) {
    this.vendor = vendor;
  }
}
